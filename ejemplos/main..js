//ejemplos de uso de switch

let erica = (new Date().getDay());

switch (erica){
    case 0:
        alert("es domingo");
        break;
    case 1:
        alert("lunes");
        break;
    case 2:
        alert("martes");
        break;
    case 3:
        alert("es miercoles y se viene fon de semana de descanso")
        break;
    default:
        alert("es otro dia")

}

